/* - Crie um array com 10 nomes de cidades e ordene os mesmos e ordem alfabética
- Com o mesmo array ordene agora na ordem de Z-A */

let cidades = ['piracicaba', 'são paulo', 'jacutinga', 'ipeuna', 'charqueada', 'fortaleza', 'maceio', 'recife', 'aracaju', 'cuiaba']

cidades.sort() // de a -> z

console.log(cidades)

cidades.sort().reverse()

console.log(cidades) // de z -> a
